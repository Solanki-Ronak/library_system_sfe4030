    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
 try {
        String bookid = txtbookid.getText();
        String bookname = txtbookname.getText();
        String author = txtauthor.getText();
        Object coursecode = txtcoursecode.getText();
        String available = txtavailable.getText();
        
        
        

        // Check if the user has entered both student ID and password
        if (bookid.isEmpty() || bookname.isEmpty()|| author.isEmpty()|| available.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter all the details.");
            return; // Exit the method if the fields are empty
        }

        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost/library", "root", "");

        // Insert into addbook table
        PreparedStatement addBookStatement = con.prepareStatement("INSERT INTO addbook (bookid, bookname, coursecode, author, available, imageupload) VALUES (?, ?, ?, ?, ?, ?)");
        addBookStatement.setString(1, bookid);
        addBookStatement.setString(2, bookname);
        addBookStatement.setString(3, (String) coursecode);
        addBookStatement.setString(4, author);
        addBookStatement.setString(5, available);

        // Check if an image file is selected
        if (filename != null) {
            FileInputStream fis = new FileInputStream(filename);
            addBookStatement.setBinaryStream(6, fis, (int) new File(filename).length());
        } else {
            addBookStatement.setNull(6, java.sql.Types.BLOB);
        }

        addBookStatement.executeUpdate();

        // Insert into unreserved table
        PreparedStatement unreservedStatement = con.prepareStatement("INSERT INTO unreserved (bookid, bookname, coursecode, author, available, imageupload) VALUES (?, ?, ?, ?, ?, ?)");
        unreservedStatement.setString(1, bookid);
        unreservedStatement.setString(2, bookname);
        unreservedStatement.setString(3, (String) coursecode);
        unreservedStatement.setString(4, author);
        unreservedStatement.setString(5, available);

        // Check if an image file is selected
        if (filename != null) {
            FileInputStream fis = new FileInputStream(filename);
            unreservedStatement.setBinaryStream(6, fis, (int) new File(filename).length());
        } else {
            unreservedStatement.setNull(6, java.sql.Types.BLOB);
        }

        unreservedStatement.executeUpdate();
 PreparedStatement addBookStatement1 = con.prepareStatement("INSERT INTO difference (bookid,  available) VALUES (?, ?)");
        addBookStatement1.setString(1, bookid);
        addBookStatement1.setString(2, available);
        addBookStatement1.executeUpdate();
        
        con.close();

        JOptionPane.showMessageDialog(this, "The book is successfully added");
        clearFields();
    } catch (Exception ex) {
        ex.printStackTrace();
        Logger.getLogger(Adminaddbook1.class.getName()).log(Level.SEVERE, null, ex);
    }
    
    }//GEN-LAST:event_jButton2ActionPerformed

    private void UploadbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UploadbuttonActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        File f=chooser.getSelectedFile();
        filename=f.getAbsolutePath();
        ImageIcon imageicon=new ImageIcon(new ImageIcon(filename).getImage().getScaledInstance(imagetobeshown.getWidth(), imagetobeshown.getHeight(), Image.SCALE_SMOOTH));
        imagetobeshown.setIcon(imageicon);
        try{
            File image=new File(filename);
           FileInputStream fis=new FileInputStream(image);
           ByteArrayOutputStream bos=new ByteArrayOutputStream();
           byte[] buf=new byte[1024];
           for(int readNum;(readNum=fis.read(buf))!=-1;){
               bos.write(buf,0,readNum);
           }
           person_image=bos.toByteArray();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
         
    
    

    }//GEN-LAST:event_UploadbuttonActionPerformed

