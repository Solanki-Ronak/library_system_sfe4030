    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String firstname = txtadfirstname.getText();
        String lastname = txtadlastname.getText();
        String email = txtademail.getText();
        String adminid = txtadminid.getText();
        String password = txtpassword.getText();
        String copassword = txtcopassword.getText();

        // Check if any of the fields are empty
        if (firstname.isEmpty() || lastname.isEmpty() || email.isEmpty() || adminid.isEmpty() || password.isEmpty() || copassword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all the fields.");
            return;
        }

        if (!password.equals(copassword)) {
            JOptionPane.showMessageDialog(this, "Password and Confirm Password do not match. Please confirm your password again.");
            return;
        }

        try {
            // Check if the admin ID already exists in the database
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/library", "root", "");
            PreparedStatement ps = con.prepareStatement("SELECT adminid FROM adminsignup WHERE adminid = ?");
            ps.setString(1, adminid);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "Admin ID already exists. Please enter a different ID.");
                rs.close();
                ps.close();
                con.close();
                return;
            }

            rs.close();
            ps.close();

            // Insert the new admin record into the database
            PreparedStatement insertPs = con.prepareStatement("INSERT INTO adminsignup (adfirstname, adlastname, ademail, adminid, adpassword, adcopassword) VALUES (?, ?, ?, ?, ?, ?)");
            insertPs.setString(1, firstname);
            insertPs.setString(2, lastname);
            insertPs.setString(3, email);
            insertPs.setString(4, adminid);
            insertPs.setString(5, password);
            insertPs.setString(6, copassword);
            // There's no need to set the confirm password field in the database
            insertPs.executeUpdate();

            JOptionPane.showMessageDialog(this, "Registered Successfully");

            // Clear the text fields
            txtadfirstname.setText("");
            txtadlastname.setText("");
            txtademail.setText("");
            txtadminid.setText("");
            txtpassword.setText("");
            txtcopassword.setText("");

            con.close();
        } catch (Exception ex) {
            Logger.getLogger(Adminsignuppage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed
