    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 String firstname = txtfirstname.getText();
    String lastname = txtlastname.getText();
    String email = txtemail.getText();
    String studentid = txtstudentid.getText();
    String major = txtmajor.getText();
    String password = txtpassword.getText();
    String copassword = txtcopassword.getText();

    // Check if any of the fields are empty
    if (firstname.isEmpty() || lastname.isEmpty() || email.isEmpty() || studentid.isEmpty() ||
            major.isEmpty() || password.isEmpty() || copassword.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all the fields.");
        return;
    }

    if (!password.equals(copassword)) {
        JOptionPane.showMessageDialog(this, "Password and Confirm Password do not match. Please confirm your password again.");
        return;
    }

    try {
        // Check if the student ID already exists in the database
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost/library", "root", "");
        PreparedStatement ps = con.prepareStatement("SELECT studentid FROM signup WHERE studentid = ?");
        ps.setString(1, studentid);
        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            JOptionPane.showMessageDialog(this, "Student ID already exists. Please enter a different ID.");
            rs.close();
            ps.close();
            con.close();
            return;
        }

        rs.close();
        ps.close();

        // Insert the new student record into the database
        PreparedStatement insertPs = con.prepareStatement("INSERT INTO signup (firstname, lastname, email, studentid, major, password, copassword) VALUES (?, ?, ?, ?, ?, ?, ?)");
        insertPs.setString(1, firstname);
        insertPs.setString(2, lastname);
        insertPs.setString(3, email);
        insertPs.setString(4, studentid);
        insertPs.setString(5, major);
        insertPs.setString(6, password);
        insertPs.setString(7, copassword);
        // There's no need to set the confirm password field in the database
        insertPs.executeUpdate();

        JOptionPane.showMessageDialog(this, "Registered Successfully");

        // Clear the text fields
        txtfirstname.setText("");
        txtlastname.setText("");
        txtemail.setText("");
        txtstudentid.setText("");
        txtmajor.setText("");
        txtpassword.setText("");
        txtcopassword.setText("");

        con.close();
    } catch (Exception ex) {
        Logger.getLogger(Studentsignuppage.class.getName()).log(Level.SEVERE, null, ex);
    }

    }//GEN-LAST:event_jButton1ActionPerformed

