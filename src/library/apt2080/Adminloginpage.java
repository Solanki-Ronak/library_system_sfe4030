    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
String adminID = txtadminid.getText();
String password = txtadminpassword.getText();

if (adminID.equals("1212") && password.equals("hello")) {
    // Correct admin ID and password, open Adminfirstpageregister
    Adminfirstpageregister adminFirstPageRegister = new Adminfirstpageregister();
    adminFirstPageRegister.setVisible(true);
    this.dispose();
    } else if (adminID.equals("1313") && password.equals("hello")) {
    Adminfirstpageregister adminFirstPageRegister = new Adminfirstpageregister();
    adminFirstPageRegister.setVisible(true);
    this.dispose();
} else {
    try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost/library", "root", "");
         PreparedStatement ps = con.prepareStatement("SELECT * FROM adminsignup WHERE adminid = ? AND adpassword = ?")) {

        // Check if the user has entered both Admin ID and password
        if (adminID.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your Admin ID and password.");
            return; // Exit the method if the fields are empty
        }

        ps.setString(1, adminID);
        ps.setString(2, password);

        try (ResultSet rs = ps.executeQuery()) {
            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "Login Successful");
                // Clear the text fields
                txtadminid.setText("");
                txtadminpassword.setText("");
                Adminfirstpage adminFirstPage = new Adminfirstpage();
                adminFirstPage.setVisible(true);
                this.dispose(); // Close the current login page
            } else {
                JOptionPane.showMessageDialog(this, "Invalid Admin ID or password. Please try again.");
            }
        }
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error connecting to the database: " + ex.getMessage());
        ex.printStackTrace();
    }
}

    }//GEN-LAST:event_jButton1ActionPerformed

    private void chkShowPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chkShowPasswordActionPerformed
        // TODO add your handling code here:
        // Toggle the state of the showPassword variable
         showPassword = !showPassword;

    // Show or hide the password based on the state of the checkbox
    if (showPassword) {
        txtadminpassword.setEchoChar((char) 0); // Show the password as plain text
    } else {
        txtadminpassword.setEchoChar('*'); // Show the password as masked text
    }
    }//GEN-LAST:event_chkShowPasswordActionPerformed

